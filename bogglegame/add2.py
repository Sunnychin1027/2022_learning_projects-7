import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    #######################
    # l1: 2->4->3
    # l2: 5->6->4
    # put l1, l2 into list
    list1 = []     # [2, 4, 3]
    list2 = []     # [5, 6, 4]
    while l1 is not None:
        list1.append(l1.val)
        l1 = l1.next
    while l2 is not None:
        list2.append(l2.val)
        l2 = l2.next
    # Calculate l1 and l2
    sum1 = 0
    sum2 = 0
    for i in range(len(list1)):
        sum1 = list1[i] * (10**i) + sum1
    for i in range(len(list2)):
        sum2 = list2[i] * (10**i) + sum2
    # Add up l1 and l2, 708
    ans = str(sum1 + sum2)
    # Convert sum to linked list
    l3 = len(ans) # 3
    result = ListNode(int(ans[0]))  #7
    for i in range(1, l3):
        result = ListNode(int(ans[i]), result)
    return result
